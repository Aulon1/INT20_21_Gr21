  Sheqeri mund të ketë ndikim ngushëllues në momente stresuese por ky ndikim është afatshkurtër dhe mund të krijojë varësi.</br>
Studimet të cilave u referohet Agroweb thonë se pas konsumit të vazhdueshëm të sheqerit, në tru aktivizohet modaliteti, që pengon sekretimin normal të dopaminës, por pritjes së variantit kimik të saj që vjen nga një substancë stimuluese, siç është sheqeri, alkoli apo droga.Me kalimin e kohës, truri krijon varësi ndaj burimeve të jashtme të dopaminës ama përjeton një stres të fortë ushqimor. Mesazhi i shpërblesës në tru është veçse ‘Kali i Trojës’ për përhapjen e inflamacionit në tru dhe çrregullimeve në nivelin e sheqerit në gjak. Qendra Amerikane për Kontrollin dhe Parandalimin e Sëmundjeve thotë se konsumi i sheqerit duhet të jetë sa më i pakët, sepse ai kontribuon në shumë probleme shëndetësore që rrisin predispozitën e përkeqësimit të simptomave të COVID-19.</br></br>
Ekspertët paralajmërojnë se të rriturit e çdo moshe që kanë sëmundje shoqëruese ose të fshehura janë më të rrezikuar nga koronavirusi. Ata janë më të predispozuar jo vetëm të kenë nevojë për ndihmë spitalore apo për intubim por edhe të humbin jetën.
</br>
Sëmundjet që lidhen direkt me mënyrën si ushqehemi e që rrisin rrezikun e komplikacioneve prej COVID-19 janë:</br>
Sëmundje të zemrës </br>
Mbipesha dhe obeziteti</br>
Diabeti mellitus (DM tip 2)</br>
Deri më tani ekspertët nuk kanë arritur të përcaktojnë përse problemet me metabolizmin dhe obeziteti rrisin rrezikun e komplikacioneve të rënda prej COVID-19. Nuk është ende e qartë nëse faktori kryesor është rezistenca ndaj insulinës apo dhjamosja e mëlçisë, apo të gjithë bashkë.

Ajo që dihet është se një njeri obez e ka tepër të vështirë të marrë frymë kur ka mushkëri të infektuara nga COVID dhe është shumë e vështirë që një person i tillë të marrë kujdesin e duhur dhe të shpejtë, për shkak të problematikave të shumta shëndetësore.
  
